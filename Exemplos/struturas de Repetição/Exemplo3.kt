//Estruturas de Repetição - While - Exemplo 1 (Aula 02 (Março) - Kotlin)

fun main(){

    var x = 0
    while (x < 10){

        //toString converte a variável para uma String
        println(x.toString())
        //x++ é um contador, dentro de uma estrutura de repetição ele vai mostrar os números dentro do parâmetro.
        x++
    }
}